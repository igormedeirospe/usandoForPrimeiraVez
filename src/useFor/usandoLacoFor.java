package useFor;

public class usandoLacoFor {
    public static void main(String[] args) {

        //O laço "for" em Java é uma estrutura de repetição utilizada para executar um bloco de código um número específico de vezes, geralmente
        // conhecida pelo número de iterações, que pode ser determinada pelo tamanho de um array ou por um contador definido.

        int numero;
        numero = 5;
        for (numero = numero; numero <= 11; numero++){
                System.out.println(numero);
        }

    }
}
